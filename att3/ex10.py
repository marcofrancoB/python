# Criando o dicionário com as informações da pessoa
pessoa = {
    "first_name": "Marco",
    "last_name": "Borges",
    "age": 19,
    "city": "Brasília"
}

# Mostrando cada informação armazenada no dicionário
print("Primeiro nome:", pessoa["first_name"])
print("Sobrenome:", pessoa["last_name"])
print("Idade:", pessoa["age"])
print("Cidade:", pessoa["city"])
